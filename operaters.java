public class operaters {
    public static void main(String[] args) {
        int a=45,b=37;
        
        System.out.print("The value of a+b is ");
        System.out.println(a+b);
        System.out.print("The value of a-b is ");
        System.out.println(a-b);
        System.out.print("The value of a*b is ");
        System.out.println(a*b);
        System.out.print("The value of a/b is ");
        System.out.println(a/b);
        System.out.print("The value of a+%b is ");
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(b++);
        System.out.println(a--);
        System.out.println(b--);
        System.out.println(++a);
        System.out.println(++b);
        System.out.println(--a);
        System.out.println(--b);


    
}
}
